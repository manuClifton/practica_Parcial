
/** \brief Despliega menu de opciones en pantalla, con posibilidad de ingresar un entero.
 *
 * \return Devuelve el número entero ingresado por el usuario.
 *
 */
int showMenu();


/** \brief Realiza la operación suma de parametros.
 *
 * \param Primer número entero.
 * \param Segundo número entero.
 * \return Devuelve la resolución de la operación en un entero.
 *
 */
int sumOperation(int num1, int num2);


/** \brief Resta los parametros ingresados.
 *
 * \param Primer número entero.
 * \param Segundo número entero.
 * \return Devuelve la diferencia como un entero.
 *
 */
int subtractionOperation(int num1, int num2);


/** \brief Divide los parametros ingresados.
 *
 * \param Primer número entero.
 * \param Segundo número entero.
 * \return Devuelve la resolución de la división como un número flotante.
 *
 */
float divideOperation(int num1,int num2);


/** \brief Multiplica los parametros ingresados.
 *
 * \param Primer número entero.
 * \param Segundo número entero.
 * \return Devuelve el resultado como un entero.
 *
 */
int multipliOperation(int num1, int num2);


/** \brief Notación factorial del parametro.
 *
 * \param Número entero.
 * \return Devuelve resolución de operación como long.
 *
 */
long factorialOperationA(int num);


/** \brief Notación factorial del parametro.
 *
 * \param Número entero.
 * \return Devuelve resolución de operación como long.
 *
 */
long factorialOperationB(int num);
